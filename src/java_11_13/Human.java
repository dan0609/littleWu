package java_11_13;

public class Human {
    private int age;
    private double height;
    private int speed;

    public Human(int age, double height, int speed) {
        this.age = age;
        this.height = height;
        this.speed = speed;
    }

    public int getAge(){
        return age;
    }
    public double getHeight(){
        return height;
    }
    public int getSpeed(){
        return speed;
    }
}

