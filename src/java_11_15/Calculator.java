package java_11_15;

public class Calculator {
    //配置计算器的成员变量


    //计算器构造器
    public  Calculator(){

    }


    //加法
    public static double add(double input1,double input2){
       return input1 + input2;
    }
    //减法
    public static double subtraction(double input1,double input2){
        return input1 - input2;
    }
    //乘法
    public static double multiplication(double input1,double input2){
        return input1 * input2;
    }
    //除法
    public static double division_method(double input1,double input2){
        return input1 / input2;
    }

}
